def main():
    MONO_DIR.mkdir(parents=True, exist_ok=True)
    
    for letter, design in MONOGRAMS.items():
        filepath = MONO_DIR / f"{letter}.txt"
        # Strip leading/trailing newlines but preserve internal structure
        lines = design.strip().split("\n")
        with open(filepath, "w") as f:
            f.write("\n".join(lines))
        print(f"Created {letter}.txt")
    
    print(f"\nGenerated {len(MONOGRAMS)} monograms in {MONO_DIR}")


if __name__ == "__main__":
    main()
